numA = int(input("Digite 1 numero: "))

numB = int(input("Digite 2 numero: "))

soma = numA + numB
produto = numA * numB
divisao = numA // numB
resto = numA % numB

print(numA, "+", numB, "=", soma)
print(numA, "*", numB, "=", produto)
print(numA, "/", numB, "=", divisao)
print(numA, "%", numB, "=", resto)
